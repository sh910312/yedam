package yedamOracle.com;

public class dddd {

	public static void main(String[] args) {
		
				
		int i = P (1,2);
		System.out.println(i);
		
		
		int ii = M (1,2);
		System.out.println(ii);
		
		
		int iii = G (1,2);
		System.out.println(iii);
		
	}

	
	
	public static int P(int a, int b) {
		
		return(a+b);
	}
	
	public static int M(int c, int d) {
		
		return(c-d);
	}
	
	public static int G(int e, int f) {
		
		return(e*f);
	}
		
		
	
	


	
	
}